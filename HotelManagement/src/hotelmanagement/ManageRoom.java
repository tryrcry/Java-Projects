    private void btnRoomAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRoomAddActionPerformed
        // TODO add your handling code here:
        int number=Integer.valueOf(txtRoomNo.getText());
        int type=Integer.valueOf(comboRoomType.getSelectedItem().toString());
        String phn=txtRoomPhn.getText();
        String reserved="No";
        if(radioRoomYes.isSelected())
            reserved="Yes";
        if(txtRoomNo.getText().trim().equals("")||phn.trim().equals(""))
        {
            JOptionPane.showMessageDialog(rootPane,"Please enter all the values","Empty Fields",JOptionPane.WARNING_MESSAGE);
        }
        else
        {
        if(rooms.addRoom(number, type, phn,reserved))
        {
            JOptionPane.showMessageDialog(rootPane,"New Room added successfully","Add Room",JOptionPane.INFORMATION_MESSAGE);
        }
        else
        {
            JOptionPane.showMessageDialog(rootPane,"Sorry, records can't be added","Add Room Error",JOptionPane.INFORMATION_MESSAGE);
        }
        }
    }//GEN-LAST:event_btnRoomAddActionPerformed

