    private void tableRoomMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableRoomMouseClicked
        // TODO add your handling code here:
         DefaultTableModel dtm=(DefaultTableModel)tableRoom.getModel();
        int rIndex=tableRoom.getSelectedRow();
        txtRoomNo.setText(dtm.getValueAt(rIndex,0).toString());
        comboRoomType.setSelectedItem(Integer.valueOf(dtm.getValueAt(rIndex,1).toString()));
        txtRoomPhn.setText(dtm.getValueAt(rIndex,2).toString());
        String s=dtm.getValueAt(rIndex, 3).toString();
        if(s.equals("Yes"))
        {
            radioRoomYes.setSelected(true);
        }
        else
        {
            radioRoomNo.setSelected(true);
        }
    }//GEN-LAST:event_tableRoomMouseClicked

