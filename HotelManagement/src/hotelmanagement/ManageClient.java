    private void btnClientAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnClientAddActionPerformed
        // TODO add your handling code here:
        String fname=txtClientFname.getText();
        String lname=txtClientLname.getText();
        String no=txtClientNo.getText();
        String mail=txtClientMail.getText();
        if(fname.trim().equals("")||lname.trim().equals("")||no.trim().equals("")||mail.trim().equals(""))
        {
            JOptionPane.showMessageDialog(rootPane,"Please enter all the values","Empty Fields",JOptionPane.WARNING_MESSAGE);
        }
        else
        {
        if(client.addClient(fname, lname, no, mail))
        {
            JOptionPane.showMessageDialog(rootPane,"New client added successfully","Add Client",JOptionPane.INFORMATION_MESSAGE);
        }
        else
        {
            JOptionPane.showMessageDialog(rootPane,"Sorry, records can't be added","Add Client Error",JOptionPane.INFORMATION_MESSAGE);
        }
        }
    }//GEN-LAST:event_btnClientAddActionPerformed

