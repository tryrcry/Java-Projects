    private void tableClientMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableClientMouseClicked
        // TODO add your handling code here:
        DefaultTableModel dtm=(DefaultTableModel)tableClient.getModel();
        int rIndex=tableClient.getSelectedRow();
        txtClientId.setText(dtm.getValueAt(rIndex,0).toString());
        txtClientFname.setText(dtm.getValueAt(rIndex,1).toString());
        txtClientLname.setText(dtm.getValueAt(rIndex,2).toString());
        txtClientNo.setText(dtm.getValueAt(rIndex,3).toString());
        txtClientMail.setText(dtm.getValueAt(rIndex,4).toString());
    }//GEN-LAST:event_tableClientMouseClicked

    private void btnClientEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnClientEditActionPerformed
        // TODO add your handling code here:
        int id=Integer.valueOf(txtClientId.getText());
        String fname=txtClientFname.getText();
        String lname=txtClientLname.getText();
        String no=txtClientNo.getText();
        String mail=txtClientMail.getText();
        if(fname.trim().equals("")||lname.trim().equals("")||no.trim().equals("")||mail.trim().equals(""))
        {
            JOptionPane.showMessageDialog(rootPane,"Please enter all the values","Empty Fields",JOptionPane.WARNING_MESSAGE);
        }
        else
        {
        if(client.editClient(id,fname,lname,no,mail))
            JOptionPane.showMessageDialog(rootPane,"Client details updated successfully","Edit Client",JOptionPane.INFORMATION_MESSAGE);
        else
            JOptionPane.showMessageDialog(rootPane,"Records can't be updated","Edit Client Error",JOptionPane.INFORMATION_MESSAGE);
        }
        txtClientId.setText("");
        txtClientFname.setText("");
        txtClientLname.setText("");
        txtClientNo.setText("");
        txtClientMail.setText("");
    }//GEN-LAST:event_btnClientEditActionPerformed

    private void btnClientRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnClientRemoveActionPerformed
        // TODO add your handling code here:
        int id=Integer.valueOf(txtClientId.getText());
        if(client.removeClient(id))
            JOptionPane.showMessageDialog(rootPane,"Client details deleted successfully","Delete Client",JOptionPane.INFORMATION_MESSAGE);
        else
            JOptionPane.showMessageDialog(rootPane,"Records can't be deleted","Delete Client Error",JOptionPane.INFORMATION_MESSAGE);
        txtClientId.setText("");
        txtClientFname.setText("");
        txtClientLname.setText("");
        txtClientNo.setText("");
        txtClientMail.setText("");
    }//GEN-LAST:event_btnClientRemoveActionPerformed

